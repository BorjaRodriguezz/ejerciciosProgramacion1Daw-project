package cursosOnline.cursoMouredvJava;

public class variablesConstantes {

	public static void main(String[] args) {
		//Variables
		//Cadena de texto
		String name = "Borja";
		System.out.println(name);
		
		//Cambio de nombre
		name = "BorjaRodriguezz";
		System.out.println(name);
		
		//Variable número entero
		int age = 22;
		System.out.println(age);
		
		//También se puede escribir var y detecta la variable sola
		var email = "borja@gmail.com";
		System.out.println(email);
		
		//Constantes
		//Es correcto definir las constantes en mayus
		final String EMAIL = "borja@gmail.com";
		System.out.println(EMAIL);
	}

}
